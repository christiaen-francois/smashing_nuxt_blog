
import axios from "axios"
let dynamicRoutes = () => {
  const routes = axios
    .get("https://css-tricks.com/wp-json/wp/v2/posts?page=1&per_page=20")
    .then(res => {
      return res.data.map(post => `/blog/${post.slug}`)
    })
  console.log("routes: ")
  console.log(routes)
  return routes
}

